import 'package:flutter/material.dart';
import 'package:translator/translator.dart';
import 'globals.dart';

final GoogleTranslator _googleTranslator = GoogleTranslator();

extension StringExtension on String {
  String get tr {
    String lang = languageNotifier.value;

    if (lang == 'en') return this;

    AppTranslations.dictionary[lang] ??= {};

    if (AppTranslations.dictionary[lang]!.containsKey(this)) {
      return AppTranslations.dictionary[lang]![this]!;
    }

    AppTranslations.dictionary[lang]![this] = this;

    _fetchDynamicTranslation(this, lang);

    return this;
  }

  Future<void> _fetchDynamicTranslation(String text, String lang) async {
    try {
      var result = await _googleTranslator.translate(text, from: 'en', to: lang);
      AppTranslations.dictionary[lang]![text] = result.text;
      translationTrigger.value++;
    } catch (e) {
      debugPrint("Translation failed for: $text - $e");
    }
  }
}

